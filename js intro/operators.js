// arithmetic operators
var x = 23;
var y = 3;

console.log(x + y); // sum
console.log(x - y); // difference
console.log(x * y); // product
console.log(x / y); // quotient
console.log(x % y); // remainder
console.log(x ** y); // power | exponential

// comparison operators

console.log(12 > 10); // greater than
console.log(12 < 10); // lesser than
console.log(12 >= 10); // greater than or equals to
console.log(12 <= 10); // lesser than or equals to

// equals to (compares value)
console.log(12 == "12");
// strict equals to (value and datatype)
console.log(12 === "12");

// not equals (compares value)
console.log(12 != 12);
console.log(13 != 12);
console.log("13" != 12);
console.log("12" != 12);

// strict not equals (compares value and data type)
console.log(12 !== 12);
console.log(13 !== 12);
console.log("13" !== 12);
console.log("12" !== 12);

